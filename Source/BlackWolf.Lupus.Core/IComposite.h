#pragma once

#include "Utility.h"

namespace Lupus {
    template <typename Component, typename Container = Set<Component>>
    class IComposite : public Component
    {
    public:
        virtual ~IComposite() = default;

        virtual void Add(const Component& component) = 0;
        virtual void Clear() = 0;
        virtual void Remove(const Component& component) = 0;
        virtual const Container& Children() const = 0;
    };

    template <typename Component, typename Container = Set<Pointer<Component>>>
    class ICompositeReference : public Component
    {
    public:
        virtual ~ICompositeReference() = default;

        virtual void Add(const Pointer<Component>& component) = 0;
        virtual void Clear() = 0;
        virtual void Remove(const Pointer<Component>& component) = 0;
        virtual const Container& Children() const = 0;
    };
}
